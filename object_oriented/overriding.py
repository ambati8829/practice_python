#Already created a method in a super class then implementing same method in a subclass with different logic and accessing in subclass. If it overrides superclass method, then it is called as method overriding.

#Rules for Method Overriding:
#1. Super Class and subclass must be present(Inheritance must be done between two classes)
#2. Declare two classes with same method and same parameters.
#3. Logic must be different in methods.
#4. Method overriding will done when we access the same method in subclass.if we access in superclass then the method will not override.

